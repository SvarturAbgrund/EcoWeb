    <h3>Ecosistemas Polares</h3>
    <p>Los ecosistemas polares son regiones extremadamente frías, con climas severos y
suelos que muchas veces están congelados (permafrost). Estos ecosistemas son
únicos y albergan especies adaptadas al frío extremo.</p>

<h3>Artico</h3>

<img src="../public/img/artico.png" class="ecoImg">

    <ul>
    
    <li><strong>Descripción:</strong> Región alrededor del Polo Norte, cubierta de hielo marino y tundra.</li>
    
    <li><strong>Importancia:</strong> Regula el clima global y alberga especies que dependen del hielo marino.</li>
    
    <li><strong>Ejemplos de fauna:</strong> Osos polares, focas, morsas, zorros árticos.</li>
    
    <li><strong>Ejemplos de flora:</strong> Musgos, líquenes y arbustos bajos.</li>

    </ul>

    <h3>Antártida</h3>

<img src="../public/img/Antartida.png" class="ecoImg">

<ul>
    
    <li><strong>Descripción:</strong> Ecosistema ubicado en regiones cercanas al ecuador, caracterizado por altas temperaturas y abundantes lluvias durante todo el año.</li>
    
    <li><strong>Importancia:</strong> Es fundamental para la producción de oxígeno, la regulación del clima y alberga una gran diversidad de especies.</li>
    
    <li><strong>Ejemplos de fauna:</strong> Jaguares, monos, tucanes, ranas y serpientes.</li>
    
    <li><strong>Ejemplos de flora:</strong> Árboles de gran altura, lianas, orquídeas y helechos.</li>

</ul>


<h3> Tundra</h3>
<img src="../public/img/tundra.png" class="ecoImg">

<ul>
    
    <li><strong>Descripción:</strong> Ecosistema caracterizado por climas extremadamente secos, con escasas precipitaciones y grandes variaciones de temperatura entre el día y la noche.</li>
    
    <li><strong>Clima:</strong> Muy caluroso durante el día y frío por la noche; lluvias muy escasas a lo largo del año.</li>
    
    <li><strong>Importancia:</strong> Alberga especies adaptadas a condiciones extremas y contribuye al equilibrio ecológico regional.</li>
    
    <li><strong>Ejemplos de fauna:</strong> Camellos, coyotes, serpientes, escorpiones y lagartos.</li>
    
    <li><strong>Ejemplos de flora:</strong> Cactus, nopales, arbustos espinosos y plantas suculentas.</li>

</ul>


   
